function month(monthNumber) {
    switch (monthNumber) {
        case 1:
            console.log(`January       : ${monthNumber}`);
            break;
        case 2:
            console.log(`February      : ${monthNumber}`);
            break;
        case 3:
            console.log(`March         : ${monthNumber}`);
            break;
            case 4:
            console.log(`April         : ${monthNumber}`);
            break;
            case 5:
            console.log(`May           : ${monthNumber}`);
            break;
            case 6:
            console.log(`Jun           : ${monthNumber}`);
            break;
            case 7:
            console.log(`July          : ${monthNumber}`);
            break;
            case 8:
            console.log(`August        : ${monthNumber}`);
            break;
            case 9:
            console.log(`Suptember     : ${monthNumber}`);
            break;
            case 10:
            console.log(`Octomber      : ${monthNumber}`);
            break;
            case 11:
            console.log(`November      : ${monthNumber}`);
            break;
            case 12:
            console.log(`December      : ${monthNumber}`);
            break;

    
        default:
            break;
    }
    
}
month(1);
month(12);